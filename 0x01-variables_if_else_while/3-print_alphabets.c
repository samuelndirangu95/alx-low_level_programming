#include <stdio.h>

/**
 * main - prints alphabet in lowecase then in uppercase
 * Return: 0 if Program is successful, nonzero if otherwise
 */

int main(void)
{
	char ch = 'a';
	char CH = 'A';

	while (ch <= 'z')
	{
		putchar(ch);
		ch++;
	}
	while (CH <= 'Z')
	{
		putchar(CH);
		CH++;
	}
	putchar('\n');

	return (0);
}
